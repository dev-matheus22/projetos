// parseFloat

console.log(parseFloat('12.999'));
console.log(Number.parseFloat('12.999'));

// parseInt

console.log(parseInt('10'));
console.log(parseInt(16.96));

// toFixed (limita as casas decimais)

console.log(23.99.toFixed(1));

// isNaN

console.log(isNaN(12));

// MAX_VALUE e MIN_VALUE (os maiores e menores valores que podemos usar no JS antes que eles se tornem infinity)

console.log(Number.MAX_VALUE);
console.log(Number.MIN_VALUE);